void ioshield_setup()
{
	/* Set up peripheral bus clock */
    /* OSCCONbits.PBDIV = 1; */
    OSCCONCLR = 0x100000; /* clear PBDIV bit 1 */
	OSCCONSET = 0x080000; /* set PBDIV bit 0 */
	
	/* Set up output pins */
	AD1PCFG = 0xFFFF;
	ODCE = 0x0;
	TRISECLR = 0xFF;
	PORTE = 0x0;
	
	/* Output pins for display signals */
	PORTF = 0xFFFF;
	PORTG = (1 << 9);
	ODCF = 0x0;
	ODCG = 0x0;
	TRISFCLR = 0x70;
	TRISGCLR = 0x200;
	
	/* Set up input pins */
	TRISDSET = (1 << 8);
	TRISFSET = (1 << 1);
	
	/* Set up SPI as master */
	SPI2CON = 0;
	SPI2BRG = 4;
	/* SPI2STAT bit SPIROV = 0; */
	SPI2STATCLR = 0x40;
	/* SPI2CON bit CKP = 1; */
    SPI2CONSET = 0x40;
	/* SPI2CON bit MSTEN = 1; */
	SPI2CONSET = 0x20;
	/* SPI2CON bit ON = 1; */
	SPI2CONSET = 0x8000;
}

void ioshield_quicksleep(int cyc)
{
	int i;
	for(i = cyc; i > 0; i--);
}

uint8_t ioshield_spi_send_recv(uint8_t data)
{
	while(!(SPI2STAT & 0x08));
	SPI2BUF = data;
	while(!(SPI2STAT & 1));
	return SPI2BUF;
}

void ioshield_display_init(void)
{
    DISPLAY_CHANGE_TO_COMMAND_MODE;
	ioshield_quicksleep(10);
	DISPLAY_ACTIVATE_VDD;
	ioshield_quicksleep(1000000);
	
	ioshield_spi_send_recv(0xAE);
	DISPLAY_ACTIVATE_RESET;
	ioshield_quicksleep(10);
	DISPLAY_DO_NOT_RESET;
	ioshield_quicksleep(10);
	
	ioshield_spi_send_recv(0x8D);
	ioshield_spi_send_recv(0x14);
	
	ioshield_spi_send_recv(0xD9);
	ioshield_spi_send_recv(0xF1);
	
	DISPLAY_ACTIVATE_VBAT;
	ioshield_quicksleep(10000000);
	
	ioshield_spi_send_recv(0xA1);
	ioshield_spi_send_recv(0xC8);
	
	ioshield_spi_send_recv(0xDA);
	ioshield_spi_send_recv(0x20);
	
	ioshield_spi_send_recv(0xAF);
}

void ioshield_display_string(int line, char *s)
{
	int i;
    
	if (line < 0 || line >= 4)
    {
		return;
    }
	if (!s)
    {
		return;
    }
	
	for(i = 0; i < 16; i++)
    {
		if (*s)
        {
			ioshield_textbuffer[line][i] = *s;
			s++;
		}
        else
        {
			ioshield_textbuffer[line][i] = ' ';
        }
    }
}

void ioshield_display_update(void)
{
	int i, j, k;
	int c;
    
	for (i = 0; i < 4; i++)
    {
		DISPLAY_CHANGE_TO_COMMAND_MODE;
		ioshield_spi_send_recv(0x22);
		ioshield_spi_send_recv(i);
		
		ioshield_spi_send_recv(0x0);
		ioshield_spi_send_recv(0x10);
		
		DISPLAY_CHANGE_TO_DATA_MODE;
		
		for(j = 0; j < 16; j++)
        {
			c = ioshield_textbuffer[i][j];
            
			if (c & 0x80)
            {
				continue;
            }
			
			for (k = 0; k < 8; k++)
            {
				ioshield_spi_send_recv(ioshield_font[c*8 + k]);
            }
		}
	}
}

#define ITOA_BUFSIZ 24

char* ioshield_itoa(int num)
{
    register int i, sign;
    static char itoa_buffer[ITOA_BUFSIZ];
    static const char maxneg[] = "-2147483648";
    
    itoa_buffer[ITOA_BUFSIZ - 1] = 0;   /* Insert the end-of-string marker. */
    sign = num;                           /* Save sign. */
    
    if (num < 0 && num - 1 > 0)          /* Check for most negative integer */
    {
        for (i = 0; i < sizeof(maxneg); i += 1)
        {
            itoa_buffer[i + 1] = maxneg[i];
        }
        
        i = 0;
    }
    else
    {
        if (num < 0)
        {
            num = -num;           /* Make number positive. */
        }
        
        i = ITOA_BUFSIZ - 2;                /* Location for first ASCII digit. */
        
        do
        {
            itoa_buffer[i] = num % 10 + '0';/* Insert next digit. */
            num = num / 10;                   /* Remove digit from number. */
            i -= 1;                           /* Move index to next empty position. */
        }
        while (num > 0);
        
        if (sign < 0)
        {
            itoa_buffer[i] = '-';
            i -= 1;
        }
    }
    /* Since the loop always sets the index i to the next empty position,
    * we must add 1 in order to return a pointer to the first occupied position. */
    return &itoa_buffer[i + 1];
}
